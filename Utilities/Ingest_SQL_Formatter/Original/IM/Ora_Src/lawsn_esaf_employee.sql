SELECT
	CMPY,
	EMPL,
	to_char(CAST(CRET_DT AS DATE), 'YYYY-MM-DD') AS CRET_DT,
	DEPT,
	EMPL_STTS,
	FRST_NM,
	to_char(CAST(HIRE_DT AS DATE), 'YYYY-MM-DD') AS HIRE_DT,
	LST_NM,
	MIDL_NM,
	NKNM,
	POSN,
	PRCS_LVL,
	SUPV,
	to_char(CAST(TERMN_DT AS DATE), 'YYYY-MM-DD') AS TERMN_DT,
	ADDR_1,
	ADDR_2,
	CITY,
	ST,
	ZIP,
	'v_currtimestamp' AS dw_last_update_date_time
FROM
	(
	SELECT
		T1.CMPY,
		T1.EMPL,
		T1.CRET_DT,
		T1.DEPT,
		T1.EMPL_STTS,
		T1.FRST_NM,
		T1.HIRE_DT,
		T1.LST_NM,
		T1.MIDL_NM,
		T1.NKNM,
		T1.POSN,
		T1.PRCS_LVL,
		T1.SUPV,
		T1.TERMN_DT,
		T1.ADDR_1,
		T1.ADDR_2,
		T1.CITY,
		T1.ST,
		T1.ZIP
	FROM
		ORAFS.VW_ESAF_EMPLOYEE T1
UNION
	SELECT
		T2.CMPY,
		T2.EMPL,
		T2.CRET_DT,
		T2.DEPT,
		T2.EMPL_STTS,
		T2.FRST_NM,
		T2.HIRE_DT,
		T2.LST_NM,
		T2.MIDL_NM,
		T2.NKNM,
		T2.POSN,
		T2.PRCS_LVL,
		T2.SUPV,
		T2.TERMN_DT,
		T2.ADDR_1,
		T2.ADDR_2,
		T2.CITY,
		T2.ST,
		T2.ZIP
	FROM
		ORACORP.VW_ESAF_EMPLOYEE T2
)