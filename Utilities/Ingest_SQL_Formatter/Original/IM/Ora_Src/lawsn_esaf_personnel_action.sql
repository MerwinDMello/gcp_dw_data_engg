SELECT
ACTN_CD,
CMPY,
to_char(CAST(EFF_DT AS DATE), 'YYYY-MM-DD') AS EFF_DT,
EMPL,
	'v_currtimestamp' AS dw_last_update_date_time
FROM
(
SELECT DISTINCT
T1.ACTN_CD,
T1.CMPY,
T1.EFF_DT,
T1.EMPL
FROM ORAFS.VW_ESAF_PERSONNEL_ACTION T1

UNION

SELECT
	DISTINCT
T2.ACTN_CD,
	T2.CMPY,
	T2.EFF_DT,
	T2.EMPL
FROM
	ORACORP.VW_ESAF_PERSONNEL_ACTION T2
)